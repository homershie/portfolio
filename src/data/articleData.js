  <h2>經典的老靈魂－傳統時代 （19世紀初－20世紀中葉）</h2>
  <p>
    介紹現代之前，還必須先瞭解什麼是古典和傳統，傳統時代起源於第二次工業革命，機械印刷機取代人力印刷，帶來印刷刊物的發展，到20世紀初現代主義在視覺領域橫空出世，傳統時代的風格還一直持續發展到20世紀中葉左右，甚至在現代台灣的老店面都能觀察到傳統時代的影響。
