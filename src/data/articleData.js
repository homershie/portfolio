  <p>
    近期也有看到圖案（Pattern）類型的插畫有漸漸傾向原始主義（Primitivism）的趨勢，各類花草、藤蔓等等圖案，常常作為復古元素使用在平面設計中；在服裝方面從2019一直流行到今年2020的夏威夷襯衫，也是直接把花草穿上身的不滅單品。這些跡象雖然無法推測新藝術風格可能復甦，但也間接說明人們對植物、花卉的喜愛，不會受時間影響，下次不訪試試看一些新藝術風格的元素或表現手法吧！
  </p>

